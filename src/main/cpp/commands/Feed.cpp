#include "commands/Feed.h"
#include "Robot.h"

// ==========================================================================

Feed::Feed()
:	frc::Command("Feed") {
	Requires(Robot::shooter.get());
}

// ==========================================================================

void Feed::Initialize() {
}

// ==========================================================================

void Feed::Execute() {
	Robot::shooter->Feed();
}

// ==========================================================================

bool Feed::IsFinished() {
	return true;
}

// ==========================================================================

void Feed::End() {
	Robot::shooter->FeedStop();
}

// ==========================================================================

void Feed::Interrupted() {
	End();
}

// ==========================================================================
